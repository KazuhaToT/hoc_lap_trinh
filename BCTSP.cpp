#include <bits/stdc++.h>
using namespace std;
int n;
int arr[16][16], tmp[17], chuaxet[17]={0};
int total=0, cmin=INT_MAX;
int totalmax=INT_MAX;
void nhap(){
    cin >> n;
    for(int i = 1; i <= n;i++){
        for(int j = 1; j <= n; j++){
            cin >> arr[i][j];
            if(arr[i][j]!=0)
            cmin=min(cmin, arr[i][j]);
        }
    }
    chuaxet[1]=1;
    tmp[1]=1;
}
void Try(int a){
    for(int i=2; i <= n; i++){
        if(chuaxet[i]==0){
            tmp[a]=i;
            chuaxet[i]=1;
            total+=arr[tmp[a-1]][i];
            if(a==n){
                if(total+arr[i][1]<totalmax) totalmax=total+arr[i][1];

            }
            else if(total+(n-a+1)*cmin<totalmax)Try(a+1);
            chuaxet[i]=0;
            total-=arr[tmp[a-1]][i];
        }
    }
}
int main() {
    nhap();
    Try(2);
    cout<<totalmax<<endl;
    return 0;
}
