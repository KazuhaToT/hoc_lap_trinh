
#include<bits/stdc++.h>
using namespace std;
void sx(long long s[], int a, int b){
    int i=a, j=b;
    int pivot=s[(i+j)/2];
    while(i<=j){
        while( s[i] < pivot ) i++;
        while(s[j]>pivot) j--;
        if(i<=j){
            swap(s[i], s[j]);
            i++;
            j--;
        }
    }
    if(a<j){
        sx(s, a, j);
    }
    if (b>i){
        sx(s, i, b);
    }
}
int main(){
    int n, k;
    cin >>n >>k;
    long long s[100001];
    for(int i=0; i<n; i++){
        cin >> s[i];
    }
    sx(s, 1, n-1);
    long long sum=s[0];
    for(int i=1; i<=n-k-1; i++){
        sum -=s[i];
    }
    for(int i=n-k; i<n;i++){
        sum+=s[i];
    }
    cout << sum;
} 
