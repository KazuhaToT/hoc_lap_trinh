#include<bits/stdc++.h>
using namespace std;
#define du 1000000007
long long luythua(int a, long long b){
    if(b==0) return 1;
    long long t=luythua(a,b/2)%du;
    if(b%2==0)return (t*t)%du;
    else return (a*((t*t)%du))%du;
}
int main() {
    while(1){
        long long  b;
        int a;
        cin >> a >> b;
        if(a==0&&b==0) break;
        long long tmp=luythua(a,b);
        cout<<tmp<<endl;
    }
    return 0;
}
