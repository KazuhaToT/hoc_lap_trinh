#include <bits/stdc++.h>
using namespace std;
int bsearch(int arr[], int value, int left, int right){
    int tmp=-1;
    while(left <= right){
        int mid=(left+right)/2;
        if(arr[mid] <= value){
            tmp=mid;
            left=mid+1;
        }
        else {
            right=mid-1;
        }
    }
    return tmp;
}
int main() {
   int N, M;
   cin >> N >> M;
   int arr[N+5];
   int dem=0;
   for(int i = 0; i <N; i++){
    int tmp=0;
    cin >> tmp;
    if(tmp<M-1){
        arr[dem++]=tmp;
    }
   } 
   sort(arr, arr+dem);
   int kt=0, max=0;
   for(int i=0; i<dem; i++){
        for(int j=i+1; j<dem; j++){
            int gtrithu3=M-arr[j]-arr[i];
            if(gtrithu3<=0){
                kt=1;
                break;
            }
            int vtri=bsearch(arr, gtrithu3,0, dem-1);
            if(vtri==i||vtri==j||vtri==-1){
                break;
            }
            int sum=arr[i]+arr[j]+arr[vtri];
            if(sum>max) max=sum;
        }
        if(kt==1)break;
   }
    cout<<max<<endl;
}
