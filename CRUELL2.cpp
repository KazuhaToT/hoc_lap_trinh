#include<bits/stdc++.h>
using namespace std;
float F(float a, float s[], int n){
    float sum=0;
    for(int i=0; i<=n; i++){
        sum+=s[i]*pow(a,n-i);
    }
    return sum;
}
float timnghiem(float left, float right, float s[], int n){
    int tmp1=0;
    while(1){
        float mid=(left+right)/2;
        if(F(left, s,n)*F(mid,s,n)<0) right=mid;
        else left=mid;
        
        int  tmp2=right*1000;
        tmp1=left*1000;
        if(tmp1==tmp2) break;
    }
    return tmp1;
}
int main(){
    int n;
    cin >> n;
    float s[n+1];
    for(int i=n; i>=0; i--){
        cin >> s[i];
    }
    int tmp=timnghiem(-1000000,1000000,s,n);
    cout << tmp << endl;
    return 0;
}
